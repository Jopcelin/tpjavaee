package bean;

public class Enseignant {
private String codeprf;
private String nom;
private String prenom;
private String adress;
private String numprf;
private String dns;
private String imail;
public String getCodeprf() {
	return codeprf;
}
public void setCodeprf(String codeprf) {
	this.codeprf = codeprf;
}
public String getNom() {
	return nom;
}
public void setNom(String nom) {
	this.nom = nom;
}
public String getPrenom() {
	return prenom;
}
public void setPrenom(String prenom) {
	this.prenom = prenom;
}
public String getAdress() {
	return adress;
}
public void setAdress(String adress) {
	this.adress = adress;
}
public String getNumprf() {
	return numprf;
}
public void setNumprf(String numprf) {
	this.numprf = numprf;
}
public String getDns() {
	return dns;
}
public void setDns(String dns) {
	this.dns = dns;
}
public String getImail() {
	return imail;
}
public void setImail(String imail) {
	this.imail = imail;
}



}
